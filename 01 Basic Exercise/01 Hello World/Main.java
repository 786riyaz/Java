// Main.java

/*
public :: Scope 
class :: Blueprint for creating objects
Main :: Name of the class
*/
public class Main {
    /*
    public :: Scope
    static :: Belongs to the class rather than instances
    void :: No return value
    main :: Entry point of the program
    String[] args :: Parameter for command-line arguments
    */
    public static void main(String[] args) {
        // Comment: Print Hello, World! to the console

        /*
        System :: Standard system class
        out :: Standard output stream
        println :: Method to print a line of text
        "Hello, World!" :: Text to be printed
        */
        System.out.println("Hello, World!");
    }
}